    private void habilitar(boolean valor) {
        rb_suma.setEnabled(valor);
        rb_presicion.setEnabled(valor);
        t_inicial.setEnabled(valor);
        t_cruzes.setEnabled(valor);
        t_mutaciones.setEnabled(valor);
        t_precision.setEnabled(valor);
        rb_manual.setEnabled(valor);
        rb_automatico.setEnabled(valor);

        if (valor == true) {
            if (rb_suma.isSelected()) {
                t_precision.setEnabled(false);
            } else {
                t_precision.setEnabled(true);
            }
        }
    }

