    private void b_inicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_inicioActionPerformed
        if (iniciado == false) {
            //Configuraciones antes de iniciar        
            p_log_evolucion.setText("");
            p_out_frase.setText("");
            b_detener.setVisible(true);
            habilitar(false);
            detener = false;

            try {
                //Obener parametros
                String frase = p_in_frase.getText();

                int funcion = 0;
                if (!rb_suma.isSelected()) {
                    funcion = 1;
                }

                int poblacion = Integer.parseInt(t_inicial.getText());
                int cruzes = Integer.parseInt(t_cruzes.getText());
                int mutaciones = Integer.parseInt(t_mutaciones.getText());
                int precision = Integer.parseInt(t_precision.getText());

                e = new Evolucionar(frase, funcion, poblacion, precision, cruzes, mutaciones);

                //Tipo de ejecucion
                if (rb_automatico.isSelected()) {
                    b_inicio.setEnabled(false);
                    new Thread(new Runnable() {

                        @Override
                        public void run() {
                            String inicio = e.getDatosIniciales();
                            p_log_evolucion.setText(inicio);

                            //Generaciones
                            while (detener == false && e.evolucionCompleta() == false) {
                                System.out.println("Generacion " + e.generacion);
                                String t1 = e.getGeneracion();
                                p_log_evolucion.setText(p_log_evolucion.getText() + t1);
                            }

                            //Al terminar de generar
                            System.out.println("Sol: " + e.getSolucionFinal());
                            p_out_frase.setText(e.getSolucionFinal());
                            b_inicio.setEnabled(true);
                            b_detener.setVisible(false);
                            JOptionPane.showMessageDialog(contexto(), "La evolucion se ha completado :D", "Evolucion Completa", JOptionPane.INFORMATION_MESSAGE);                            
                            habilitar(true);
                            detener = true;
                            
                        }
                    }).start();
                } else {
                    iniciado = true;
                    String inicio = e.getDatosIniciales();
                    p_log_evolucion.setText(inicio);                    
                    b_inicio.setText("Siguiente Generacion");
                }

            } catch (Exception ex) {
                Logger.getLogger(Ventana.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Problema", JOptionPane.INFORMATION_MESSAGE);

                habilitar(true);
                b_detener.setVisible(false);
            }
        } else {
            if (e.evolucionCompleta()) {
                System.out.println("Sol: " + e.getSolucionFinal());
                p_out_frase.setText(e.getSolucionFinal());
                b_detener.setVisible(false);                
                b_inicio.setText("Iniciar");
                JOptionPane.showMessageDialog(contexto(), "La evolucion se ha completado :D", "Evolucion Completa", JOptionPane.INFORMATION_MESSAGE);                
                habilitar(true);
                iniciado = false;

            } else {
                System.out.println("Generacion " + e.generacion);
                String t1 = e.getGeneracion();
                p_log_evolucion.setText(p_log_evolucion.getText() + t1);
            }
        }
    }//GEN-LAST:event_b_inicioActionPerformed

